; --------------------------------------------------------------------------
; 名称: 控制台蓝色背景
; 说明: 在 SB2000 平台上将控制台背景设置为蓝色的最小示例
; 作者: Lion
; 邮箱: chengbin@3578.cn
; 日期: 2025-09-23
; 备注: 适配 cc65 汇编与 sb2k.cfg 链接脚本，可直接生成 SB-DOS 可运行 .exe
; --------------------------------------------------------------------------
.segment "CODE"
	; SB-DOS 可启动标识（若需 SB-DOS EXE 头，必须紧跟在 JMP 之后）
	JMP START
	.byte $AA, $55, $00, $00, $00

START:
	; 背景色寄存器: $2040
	; 位说明: D3-D0 = 颜色编号 (1 = 蓝色)
	;         D5-D4 = 亮度 (1 = 正常)
	; 设定值: $01
	LDA #$01
	STA $2040

WAIT_LOOP:
	JMP WAIT_LOOP

; --------------------------------------------------------------------------
; 中断/复位向量区（适配 sb2k.cfg）
; --------------------------------------------------------------------------
.segment "VECTORS"
	.word 0	; NMI
	.word START	; RESET
	.word 0	; IRQ